"""HW dataset loaders.

Author: Anthony Poole

Loads HW2D HDF5 trajectories generated by `hw.dns` (i.e. the upstream `hw2d`
output schema) and produces:

  - whole-trajectory tensors          for OpInf-style baselines
  - (snippet, future-rollout) pairs   for context-conditioned ROMs

The HDF5 schema is the upstream hw2d schema:

  /density  : (n_t, n_y, n_x)  float
  /phi      : (n_t, n_y, n_x)  float
  /omega    : (n_t, n_y, n_x)  float
  /gamma_n  : (n_t,)           float   (hw2d-native, computed at sim res)
  /gamma_c  : (n_t,)           float
  /...      : other properties (energy, enstrophy, ...)

Plus root attributes: dt, c1 (=alpha), k0, nu, N, age, ...

Convention: we always discard the burn-in stretch (first half of the file by
default) before any training or evaluation.
"""

from __future__ import annotations

import dataclasses
from pathlib import Path

import numpy as np


@dataclasses.dataclass
class HwTrajectory:
    """One loaded HW DNS trajectory at a single alpha."""

    density: np.ndarray
    phi: np.ndarray
    gamma_n: np.ndarray
    gamma_c: np.ndarray
    dt: float
    alpha: float
    grid: dict


def load_trajectory(
    h5_path: str | Path,
    burn_in_frac: float = 0.5,
) -> HwTrajectory:
    """Load a trajectory and discard burn-in.

    Parameters
    ----------
    h5_path : path
        Path to a `trajectory.h5` produced by `hw.dns`.
    burn_in_frac : float
        Fraction of the file to discard as transient. Default 0.5.
    """
    import h5py  # noqa: PLC0415

    from hw.physics import grid

    h5_path = Path(h5_path)
    with h5py.File(h5_path, "r") as hf:
        n_t_total = hf["density"].shape[0]
        start = int(burn_in_frac * n_t_total)

        density = np.asarray(hf["density"][start:], dtype=np.float64)
        phi = np.asarray(hf["phi"][start:], dtype=np.float64)
        gamma_n = (
            np.asarray(hf["gamma_n"][start:], dtype=np.float64)
            if "gamma_n" in hf
            else np.array([])
        )
        gamma_c = (
            np.asarray(hf["gamma_c"][start:], dtype=np.float64)
            if "gamma_c" in hf
            else np.array([])
        )

        dt = float(hf.attrs.get("dt", np.nan))
        alpha = float(hf.attrs.get("c1", np.nan))
        k0 = float(hf.attrs.get("k0", 0.15))

    g = grid(k0=k0, nx=density.shape[-1])
    return HwTrajectory(
        density=density, phi=phi,
        gamma_n=gamma_n, gamma_c=gamma_c,
        dt=dt, alpha=alpha, grid=g,
    )


def iter_snippets(
    traj: HwTrajectory,
    snippet_len: int,
    horizon: int,
    stride: int = 1,
):
    """Yield (snippet, future) tuples from a trajectory for ROM training.

    Each yielded item is:
        snippet  : (snippet_len, 2, n_y, n_x)   density+phi stacked
        future   : (horizon,     2, n_y, n_x)   the next `horizon` frames
    """
    n_t = traj.density.shape[0]
    last = n_t - snippet_len - horizon + 1
    for k in range(0, last, stride):
        s_dens = traj.density[k : k + snippet_len]
        s_phi = traj.phi[k : k + snippet_len]
        f_dens = traj.density[k + snippet_len : k + snippet_len + horizon]
        f_phi = traj.phi[k + snippet_len : k + snippet_len + horizon]
        snippet = np.stack([s_dens, s_phi], axis=1)   # (T, 2, H, W)
        future = np.stack([f_dens, f_phi], axis=1)
        yield snippet, future


# === sanity check ===

if __name__ == "__main__":
    import sys

    if len(sys.argv) < 2:
        print("usage: python -m hw.dataset <path/to/trajectory.h5>")
        sys.exit(1)

    traj = load_trajectory(sys.argv[1])
    print(
        f"alpha={traj.alpha} dt={traj.dt} "
        f"shape(density)={traj.density.shape} "
        f"<gamma_n>={traj.gamma_n.mean():+.4f} "
        f"+- {traj.gamma_n.std():.4f}"
    )
